#region Операторы логического И (and).

print(f'Положительный ответ : ', True and True)
print(f'Положительный ответ : ', True and False)
print(f'Положительный ответ : ', False and True)
print(f'Положительный ответ : ', False and False)

#endregion


#region Операторы логического ИЛИ (or).

print(f'Положительный ответ : ', True or True)
print(f'Положительный ответ : ', True or False)
print(f'Положительный ответ : ', False or True)
print(f'Положительный ответ : ', False or False)

#endregion


#region Операторы логического НЕ (not).

print(f'Положительный ответ : ', not True)
print(f'Положительный ответ : ', not False)

#endregion