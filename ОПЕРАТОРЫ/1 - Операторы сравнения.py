"""
    - операторы сравнения
"""


#region -
print('\n')
#endregion

#region Операторы равенства ==.

print(f'Положительный ответ : ', 10 == 10)
print(f'Положительный ответ : ', 10 == 20)

#endregion


#region -
print('\n ---------- \n')
#endregion
#region Операторы неравенства !=.

print(f'Положительный ответ : ', 10 != 10)
print(f'Положительный ответ : ', 10 != 20)

#endregion

#region -
print('\n ---------- \n')
#endregion
#region Операторы больше чем >.

print(f'Положительный ответ : ', 10 > 10)
print(f'Положительный ответ : ', 10 > 20)

#endregion

#region -
print('\n ---------- \n')
#endregion
#region Операторы меньше чем <.

print(f'Положительный ответ : ', 10 < 10)
print(f'Положительный ответ : ', 10 < 20)

#endregion

#region -
print('\n ---------- \n')
#endregion
#region Операторы больше или равно >=.

print(f'Положительный ответ : ', 10 >= 10)
print(f'Положительный ответ : ', 10 >= 20)

#endregion

#region -
print('\n ---------- \n')
#endregion
#region Операторы меньше или равно <=.

print(f'Положительный ответ : ', 10 <= 10)
print(f'Положительный ответ : ', 10 <= 20)

#endregion
